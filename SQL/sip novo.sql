/*NUMERO DE MULHERES ENTRE 50 A 69 ANOS
NUMERO DE MULHERES ENTRE 25 A 59 ANOS
NUMERO DE CRIANÇAS ENTRE 0 A 5 ANOS
NUMERO DE IDOSOS (+ 60 ANOS )
NÚMERO DE IDOSOS ENTRE 59 a 69 ANOS
*/
SELECT 'NUMERO DE MULHERES ENTRE 50 A 69 ANOS', COUNT(*) FROM BA1020
WHERE BA1_CODEMP = '0001'
AND BA1_DATCON <= '20220630'
AND (BA1_DATBLO >='20220630' OR BA1_DATBLO = '')
AND BA1_SEXO = '2'
AND Datediff(yyyy,Convert(Datetime,BA1_DATNAS,03),GETDATE()) BETWEEN 50 AND 69
AND D_E_L_E_T_ = ''
UNION
SELECT 'NUMERO DE MULHERES ENTRE 25 A 59 ANOS', COUNT(*) FROM BA1020
WHERE BA1_CODEMP = '0001'
AND BA1_DATCON <= '20220630'
AND (BA1_DATBLO >='20220630' OR BA1_DATBLO = '')
AND BA1_SEXO = '2'
AND Datediff(yyyy,Convert(Datetime,BA1_DATNAS,03),GETDATE()) BETWEEN 25 AND 59
AND D_E_L_E_T_ = ''
UNION
SELECT 'NUMERO DE CRIANÇAS ENTRE 0 A 5 ANOS', COUNT(*) FROM BA1020
WHERE BA1_CODEMP = '0001'
AND BA1_DATCON <= '20220630'
AND (BA1_DATBLO >='20220630' OR BA1_DATBLO = '')
AND Datediff(yyyy,Convert(Datetime,BA1_DATNAS,03),GETDATE()) <= 5
AND D_E_L_E_T_ = ''
UNION 
SELECT 'NUMERO DE IDOSOS (+ 60 ANOS )', COUNT(*) FROM BA1020
WHERE BA1_CODEMP = '0001'
AND BA1_DATCON <= '20220630'
AND (BA1_DATBLO >='20220630' OR BA1_DATBLO = '')
AND Datediff(yyyy,Convert(Datetime,BA1_DATNAS,03),GETDATE()) >60
AND D_E_L_E_T_ = ''
UNION
SELECT 'NUMERO DE IDOSOS ENTRE 59 a 69 ANOS', COUNT(*) FROM BA1020
WHERE BA1_CODEMP = '0001'
AND BA1_DATCON <= '20220630'
AND (BA1_DATBLO >='20220630' OR BA1_DATBLO = '')
AND Datediff(yyyy,Convert(Datetime,BA1_DATNAS,03),GETDATE()) BETWEEN 59 AND 69
AND D_E_L_E_T_ = ''

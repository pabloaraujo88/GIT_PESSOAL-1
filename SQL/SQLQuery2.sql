SELECT BQC_CODIGO+BQC_NUMCON+BQC_VERCON+BQC_SUBCON+BQC_VERSUB AS CONTRATO,
BQC_DESCRI EMPRESA,BD5_CODOPE + BD5_CODEMP + BD5_MATRIC + BD5_TIPREG+BD5_DIGITO BENEFICIARIO, BD5_CODPEG PROTOCOLO, BD5_NUMERO GUIA,
convert(char(10), convert(datetime, BD5_DATPRO, 103), 103) DATA_PROCEDIMENTO, BD5_VLRPAG 
FROM BD5020 BD5 JOIN BQC020 BQC ON (BD5_CODOPE+BD5_CODEMP+BD5_CONEMP+BD5_VERCON+BD5_SUBCON+BD5_VERSUB
= BQC_CODIGO+BQC_NUMCON+BQC_VERCON+BQC_SUBCON+BQC_VERSUB)
WHERE BD5.D_E_L_E_T_ = ''
AND BQC.D_E_L_E_T_ = ''
AND BQC_CODIGO+BQC_NUMCON IN
('00010002000000000002',
'00010002000000000017',
'00010002000000000019')
AND BD5_FASE = '4'
AND BD5_SITUAC = '1'
--AND BD5_ANOPAG = '2022'
AND BD5_VLRPAG > 0
 ORDER BY BQC_CODIGO+BQC_NUMCON+BQC_VERCON+BQC_SUBCON+BQC_VERSUB

